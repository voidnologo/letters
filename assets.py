def get_system_voices():
    output = subprocess.check_output("say -v '?'", shell=True, universal_newlines=True)
    data = output.split('\n')
    voices = []
    for datum in data:
        voice = datum.split()
        if voice and voice[1] in ['en_US', 'en_GB']:
            voices.append(voice[0])
    return voices


VOICES = get_system_voices()
